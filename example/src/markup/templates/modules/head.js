'use strict'

//----------------------------------------------------------
// modules
//----------------------------------------------------------
// npm
const m = require('mithril')

//----------------------------------------------------------
// logic
//----------------------------------------------------------
const head = data =>
  m('head',
    [ m('title', data.title)
    , m('meta', { name: 'description', content: data.description})
    ]
  )

//----------------------------------------------------------
// exports
//----------------------------------------------------------
module.exports = head
