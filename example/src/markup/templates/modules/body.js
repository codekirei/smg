'use strict'

//----------------------------------------------------------
// modules
//----------------------------------------------------------
// npm
const m = require('mithril')

//----------------------------------------------------------
// logic
//----------------------------------------------------------
const body = content => data =>
  m('body'
    , { class: data.slug.split('/').pop() || 'index' }
    , [ content(data) ]
  )

//----------------------------------------------------------
// exports
//----------------------------------------------------------
module.exports = body
